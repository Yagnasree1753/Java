package Project1;
public class WhileLoop {
    public static void main(String[] args){
        int i=0;
        while(i<5){
            System.out.println(i);
            i++;
        }

        System.out.println("--------");

        int j=6;
        do{
            j++;
            System.out.println(j);
        }while(j<5);
    }
}
